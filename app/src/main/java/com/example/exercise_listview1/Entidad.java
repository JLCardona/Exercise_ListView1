package com.example.exercise_listview1;

public class Entidad {

    private int imagen;
    private String texto;

    public Entidad(int imagen, String texto) {
        this.imagen = imagen;
        this.texto = texto;
    }

    public int getImagen() {
        return imagen;
    }

    public String getTexto() {
        return texto;
    }
}
